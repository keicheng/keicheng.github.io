$(function() {
    var $tbody = $('.verbs'),
        $sortBtns = $('.js-sort'),
        $filter = $('.js-filter'),
        $tableWrap = $('.table-responsive-scroll'),
        $emptyTip = $('.empty-tip'),
        $badge = $('.badge');

    function getOneWord(word) {
        return word.split(' ')[0];
    }

    function showEmptyBlock() {
        $tableWrap.addClass('hide');
        $emptyTip.removeClass('hide');
    }

    function hideEmptyBlock() {
        $tableWrap.removeClass('hide')
        $emptyTip.addClass('hide');;
    }

    function makeRow(_data, isFirst) {
        if (isFirst) {
            $tbody.append('<tr class="active" />');
        } else {
            $tbody.append('<tr />');
        }

        var $tr = $tbody.children('tr').last(),
            url = (_data.keyword) ? _data.keyword : _data.oneWord,
            htmlstr = (_data.zh) ? '<td data-label="中文翻譯">' + _data.zh + '</td>' : '<td />';

        htmlstr += '<td data-label="動詞原形"><a href="https://translate.google.com.tw/#en/zh-TW/' + url + '" target="_blank">' + _data.oneWord + '</a></td>';
        htmlstr += '<td data-label="過去式"><a href="https://translate.google.com.tw/#en/zh-TW/' + getOneWord(_data.past) + '" target="_blank">' + _data.past + '</a></td>';
        htmlstr += '<td data-label="過去分詞"><a href="https://translate.google.com.tw/#en/zh-TW/' + getOneWord(_data.part) + '" target="_blank">' + _data.part + '</a></td>';

        getOneWord(_data.past);

        $tr.append(htmlstr);
    }

    function rowCounter(num) {
        $badge.text(num);
    }

    function _shuffle() {
        var num = Math.random() > 0.5 ? -1 : 1;
        return num;
    }

    function _order(_data, filter) {
        var no = 1,
            isFirst = false;

        if (_data.length) {
            filter = filter || false;

            nowData = _data;

            $.each(nowData, function(index, value) {
                isFirst =  (0 === index) ? true : false;
                makeRow(value, isFirst);
            });

            rowCounter(nowData.length);
        } else {
            showEmptyBlock();
        }

        $('html, body').stop().animate({
            scrollTop: 0
        })
    }

    function _random() {
        var randomData = nowData.slice(0);

        randomData = randomData.sort(_shuffle);
        _order(randomData);
    }

    function _filter(filter) {
        var tmpData = [],
            random = ($('.js-sort.color-primary').data('sort') === 'random');

        $.each(data, function(index, value) {
            if (filter.length == 1) {
                if (value.filter === filter) tmpData.push(value);
            } else {
                if ((filter === 'book' || filter === 'left' || filter === 'right') && value.book) {
                    tmpData.push(value);
                }
            }
        });

        if (filter === 'left') tmpData = tmpData.slice(0, 55);
        if (filter === 'right') tmpData = tmpData.slice(55, 110);
        if (random) tmpData = tmpData.sort(_shuffle);

        return tmpData;
    }

    function init() {
        var tmpData = [],
            hash = window.location.hash;

        if (hash.length) {
            hash = hash.split('#')[1];

            if (hash != '*') tmpData = _filter(hash);

            $filter.children('option[value="' + hash + '"]').prop('selected', true);

        } else {
            tmpData = data.slice(0);
        }

        _order(tmpData);
    }

    function setTrActive(wt) {
        var $tr = $('.verbs').children('tr'),
            getTrHeight = $tr.outerHeight(true),
            headerHeight = 126,
            which = parseInt((wt + headerHeight) / getTrHeight, 10);

        $tr.removeClass('active').slice(0, which+1).addClass('active');
    }

    init();

    $sortBtns.on('click', function(e) {
        e.preventDefault();

        var that = $(this),
            thatSort = that.data('sort');

        $tbody.children('tr').remove();
        $sortBtns.removeClass('color-primary');

        switch (thatSort) {
            case 'order':
                var thatFilterWord = $filter.val(),
                    getFilterData = (thatFilterWord === '*') ? data : _filter(thatFilterWord);

                _order(getFilterData);
                break;
            case 'random':
                _random();
                break;
            default:
                _order(data);
        }

        that.addClass('color-primary');
    });

    $filter.on('change', function() {
        $tbody.children('tr').remove();

        var that = $(this),
            thatFilterWord = that.val(),
            hash = '',
            getFilterData = function() {
                if (thatFilterWord.length > 1) {
                    return _filter(thatFilterWord);
                } else {
                    return (thatFilterWord === '*') ? data : _filter(thatFilterWord);
                }
            };

        nowData = getFilterData();
        hash = (thatFilterWord === '*') ? window.location.pathname : '#' + thatFilterWord;

        _order(nowData, thatFilterWord);
        window.history.pushState('', document.title, hash);
    });

    if ($('html').data('device') === 'phone') {
        $(window).on('scroll', function() {
            var that = $(this),
                thatTop = that.scrollTop();

            setTrActive(thatTop);
        });
    }
});
