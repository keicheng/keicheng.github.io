var nowData = data.slice(0);

$(function() {
    var $tbody = $('.verbs'),
        $sortBtns = $('.js-sort'),
        $filterBtns = $('.js-filter');

    function getOneWord(word) {
        return word.split(' ')[0];
    }

    function makeRow(_data, _num) {
        $tbody.append('<tr />');

        var $tr = $tbody.children('tr').last(),
            url = (_data.keyword) ? _data.keyword : _data.oneWord;
            htmlstr = '<td>' + _num + '</td>';

        htmlstr += (_data.zh) ? '<td>' + _data.zh + '</td>' : '<td />';
        htmlstr += '<td><a href="https://translate.google.com.tw/#en/zh-TW/' + url + '" target="_blank">' + _data.oneWord + '</a></td>';
        htmlstr += '<td><a href="https://translate.google.com.tw/#en/zh-TW/' + getOneWord(_data.past) + '" target="_blank">' + _data.past + '</a></td>';
        htmlstr += '<td><a href="https://translate.google.com.tw/#en/zh-TW/' + getOneWord(_data.part) + '" target="_blank">' + _data.part + '</a></td>';

        getOneWord(_data.past);

        $tr.append(htmlstr);
    }

    function _shuffle() {
        var num = Math.random() > 0.5 ? -1 : 1;
        return num;
    }

    function _order(_data) {
        $.each(_data, function(index, value) {
            makeRow(value, (index + 1));
        });
    }

    function _random() {
        var randomData = nowData.slice(0);

        randomData = randomData.sort(_shuffle);
        _order(randomData);
    }

    function _filter(filter) {
        var tmpData = [],
            random = ($('.js-sort.color-primary').data('sort') === 'random');

        $.each(data, function(index, value) {
            if (value.filter === filter) tmpData.push(value);
        });

        if (random) {
            tmpData = tmpData.sort(_shuffle);
        }

        return tmpData;
    }

    _order(data);

    $sortBtns.on('click', function(e) {
        e.preventDefault();

        var that = $(this),
            thatSort = that.data('sort');

        $tbody.children('tr').remove();
        $sortBtns.removeClass('color-primary');

        switch (thatSort) {
            case 'order':
                var thatFilterWord = $('.js-filter.active').attr('data-filter'),
                    getFilterData = (thatFilterWord === '*') ? data : _filter(thatFilterWord);

                _order(getFilterData);
                break;
            case 'random':
                _random();
                break;
            default:
                _order(data);
        }

        that.addClass('color-primary');
    });

    $filterBtns.on('click', function(e) {
        e.preventDefault();
        $tbody.children('tr').remove();
        $filterBtns.removeClass('active color-success');

        var that = $(this),
            thatFilterWord = that.data('filter'),
            getFilterData = (thatFilterWord === '*') ? data : _filter(thatFilterWord);

        nowData = getFilterData;

        _order(getFilterData);
        that.addClass('active color-success');
    });
});
